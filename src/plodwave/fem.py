import numpy as np
from scipy.sparse import (csc_array, csr_array, lil_array, diags_array, kron,
                          eye_array)
import plodwave
# type hinting
from numpy.typing import NDArray


# ======================================================================
# Construction of the dg Prolongation matrix
# ======================================================================


def dg_prolongation(nelems_coarse: NDArray,
                    nelems_refine: NDArray) -> csc_array:
    """ Returns the prolongation matrix between dg meshes.

    Parameters
    ----------
    nelems_coarse : NDArray
        Number of coarse elements per dimension.
    nelems_refine : NDArray
        Number of fine elements per coarse element per dimension.

    Returns
    -------
    csc_array
        dg prolongation matrix.
    """
    d = np.size(nelems_coarse)
    dx = np.linspace(0, 1, nelems_refine[0]+1, dtype=plodwave.config.PRECISION)
    if d == 1:
        L = nelems_refine[0]*np.arange(nelems_coarse[0])[:, None]
        Q = np.arange(nelems_refine[0])
        D = _loc_prolongation(dx, dx, d)
    elif d == 2:
        # bottom left corners
        L = nelems_refine[0]*np.arange(nelems_coarse[0])\
            + (nelems_coarse[0]*np.prod(nelems_refine)
               * np.arange(nelems_coarse[1])[:, None])
        # local fine element numbering per coarse element
        Q = np.arange(nelems_refine[0])\
            + (nelems_coarse[0]
               * nelems_refine[0]*np.arange(nelems_refine[1])[:, None])
        dy = np.linspace(0, 1, nelems_refine[1]+1,
                         dtype=plodwave.config.PRECISION)[:, None]
        D = _loc_prolongation(dx, dy, d).reshape(2**d, 2**d, -1)
    # J = (Number Coarse Elements, Number Fine Elements per Coarse
    # Element, local number of DoFs)
    J = 2**d*(L.flatten()[:, None] + Q.flatten())[..., None] + np.arange(2**d)
    # K = (Number Coarse Elements, local number of DoFs)
    K = 2**d*np.arange(np.prod(nelems_coarse))[..., None] + np.arange(2**d)
    J = np.broadcast_to(J[:, None, ...],
                        (np.prod(nelems_coarse), 2**d,
                         np.prod(nelems_refine), 2**d))
    K = np.broadcast_to(K[..., None, None],
                        (np.prod(nelems_coarse), 2**d,
                         np.prod(nelems_refine), 2**d))
    D = np.broadcast_to(np.transpose(D, (0, 2, 1)),
                        (np.prod(nelems_coarse), 2**d,
                         np.prod(nelems_refine), 2**d))
    return csc_array((D.flatten(), (J.flatten(), K.flatten())))


def _loc_prolongation(interpolate_x: NDArray,
                      interpolate_y: NDArray,
                      d: int) -> NDArray:
    """ Returns the values for the local prolongation per coarse
    element.

    Parameters
    ----------
    interpolate_x : NDArray
        Values of the prolongation in x-direction.
    interpolate_y : NDArray
        Values of the prolongation in y-direction.
    d : int
        Dimension.

    Returns
    -------
    NDArray
        Local prolongation matrix per coarse element.
    """
    D = np.array([np.array([_slice(_phi(interpolate_x, interpolate_y, k, d),
                                   j, d) for j in range(2**d)],
                           dtype=plodwave.config.PRECISION)
                  for k in range(2**d)], dtype=plodwave.config.PRECISION)
    return D


def _phi(x: NDArray,
         y: NDArray,
         key: int,
         d: int) -> NDArray:
    """ Returns the values of the prolongation for the coarse nodes
    indexed with "key".

    Parameters
    ----------
    interpolate_x : NDArray
        Values of the prolongation in x-direction.
    interpolate_y : NDArray
        Values of the prolongation in y-direction.
    key : int
        Key indicating the corners of the coarse element
        3---2
        |   |
        0---1 -> d=1
    d : int
        Dimension.

    Returns
    -------
    NDArray
        Values of the prolongation per coarse node.
    """
    if d == 1:
        if key == 0:  # left corner
            return 1 - x
        if key == 1:  # right corner
            return x
    elif d == 2:
        if key == 0:  # bottom left corner
            return (1 - x) * (1 - y)
        if key == 1:  # bottom right corner
            return x * (1 - y)
        if key == 2:  # top right corner
            return x * y
        if key == 3:  # top left corner
            return (1 - x) * y


def _slice(phi: NDArray,
           key: int,
           d: int) -> NDArray:
    """ Uses the values generated by _phi and returns the correct values
    for the local nodes indicated by "key" for the fine elements.

    Parameters
    ----------
    phi : NDArray
        Values of the prolongation per coarse node constructed by _phi.
    key : int
        Key indicating the corners of the coarse element
        3---2
        |   |
        0---1 -> d=1
    d : int
        Dimension.

    Returns
    -------
    NDArray
        Values of the prolongation per coarse node constructed by _phi
        for the fine ndoes indicate by "key".
    """
    if d == 1:
        if key == 0:  # left corner
            return phi[:-1]
        if key == 1:  # right corner
            return phi[1:]
    elif d == 2:
        if key == 0:  # bottom left corner
            return phi[:-1, :-1]
        if key == 1:  # bottom right corner
            return phi[:-1, 1:]
        if key == 2:  # top right corner
            return phi[1:, 1:]
        if key == 3:  # top left corner
            return phi[1:, :-1]


def cg_prolongation(nelems_coarse: NDArray,
                    nelems_refine: NDArray) -> csc_array:
    """ Returns the cg prolongation matrix.

    Parameters
    ----------
    nelems_coarse : NDArray
        Number of coarse elements per dimension.
    nelems_refine : NDArray
        Number of fine elements per dimension per coarse element.

    Returns
    -------
    csc_array
        cg prolongation matrix.
    """
    d = np.size(nelems_coarse)
    dx = np.linspace(1, 0, nelems_refine[0]+1,
                     dtype=plodwave.config.PRECISION)[:-1]
    if d == 1:
        L = np.arange(nelems_coarse[0], dtype=np.int32)
        J = (nelems_refine[0]*np.arange(nelems_coarse[0], dtype=np.int32)
             + np.arange(2, dtype=np.int32)[:, None]).flatten()
        J = (J[:, None] + np.arange(nelems_refine[0], dtype=np.int32)).flatten()
        K = (L+np.array([[0], [1]])).flatten()
        K = np.repeat(K, nelems_refine[0])
        D = np.tile(dx, (nelems_coarse[0],))
        D = np.concatenate([D, D[::-1]])
        P = lil_array((np.prod(nelems_coarse*nelems_refine+1),
                       np.prod(nelems_coarse+1)))
        P[J, K] = D
    elif d == 2:
        J = ((nelems_refine[0]*np.arange(nelems_coarse[0], dtype=np.int32)
             + ((nelems_coarse[0]*nelems_refine[0]+1)*nelems_refine[1]
                * np.arange(nelems_coarse[1], dtype=np.int32)[:, None])
              )[:, None, :, None]
             + np.arange(nelems_refine[0], dtype=np.int32)
             + ((nelems_refine[0]*nelems_coarse[0]+1)
                * np.arange(nelems_refine[1], dtype=np.int32))[:, None, None]
             ).reshape((nelems_coarse*nelems_refine)[::-1])
        J = (J
             + np.array([0, 1, (nelems_coarse[0]*nelems_refine[0]+1)+1,
                         (nelems_coarse[0]*nelems_refine[0]+1)],
                        dtype=np.int32)[:, None, None]).flatten()
        K = (np.arange(nelems_coarse[0], dtype=np.int32)
             + (nelems_coarse[0]+1)*np.arange(nelems_coarse[1],
                                              dtype=np.int32)[:, None])
        K = (np.repeat(np.repeat(K, nelems_refine[0], axis=1),
                       nelems_refine[1], axis=0)
             + np.array([0, 1, nelems_coarse[0]+2, nelems_coarse[0]+1],
                        dtype=np.int32)[:, None, None]).flatten()
        dy = np.linspace(1, 0, nelems_refine[1]+1,
                         dtype=plodwave.config.PRECISION)[:-1]
        D = np.tile(np.outer(dy, dx), nelems_coarse[::-1])
        D = np.concatenate([D.flatten(),
                            D[:, ::-1].flatten(),
                            D[::-1, ::-1].flatten(),
                            D[::-1, :].flatten()])
        P = lil_array((np.prod(nelems_coarse*nelems_refine+1),
                       np.prod(nelems_coarse+1)))
        P[J, K] = D
    return csc_array(P)


def element_prolongation(nelems_coarse: NDArray,
                         nelems_refine: NDArray) -> csr_array:
    """ Returns a prolongation array based on coarse to fine elements.

    Parameters
    ----------
    nelems_coarse : NDArray
        Number of coarse elements per dimension.
    nelems_refine : NDArray
        Number of fine elements per dimension per coarse element.

    Returns
    -------
    csc_array
        Element-wise prolongation matrix.
    """
    d = np.size(nelems_coarse)
    J = np.arange(np.prod(nelems_coarse*nelems_refine), dtype=np.int32)
    if d == 1:
        K = np.arange(np.prod(nelems_coarse), dtype=np.int32)
        K = np.repeat(K, nelems_refine[0])
        return csr_array((np.ones_like(J, dtype=plodwave.config.PRECISION),
                          (J, K)))
    K = np.arange(np.prod(nelems_coarse),
                  dtype=np.int32).reshape(nelems_coarse, order='F')
    K = np.repeat(np.tile(K, (nelems_refine[1], 1)), nelems_refine[0],
                  axis=0).flatten('F')
    return csr_array((np.ones_like(J, dtype=plodwave.config.PRECISION),
                      (J, K)))


def cg_dof_projection(dofs: NDArray,
                      nelems: NDArray) -> csc_array:
    """ Returns restriction to DoFs as csc array.

    Parameters
    ----------
    dofs : NDArray
        DoFs.
    nelems : NDArray
        Number of elements per dimension.

    Returns
    -------
    csc_array
        DoF restrictions map.
    """
    N = np.size(dofs)
    return csc_array((np.ones_like(dofs), (dofs, np.arange(N))),
                     shape=(np.prod(nelems+1), N))


