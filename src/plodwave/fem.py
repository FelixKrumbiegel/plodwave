def get_cg2dg(elements: NDArray) -> csc_array:
    """ Returns the cg to dg matrix.

    Parameters
    ----------
    elements : NDArray
        Element array.

    Returns
    -------
    csc_array
        cg to dg matrix.
    """
    return csc_array((np.ones((np.size(elements),),
                              dtype=plodwave.config.PRECISION),
                      (np.arange(np.size(elements), dtype=np.int32),
                       elements.flatten())))


def get_cg2dg_coarse_fine(nelems_coarse: NDArray,
                          nelems_refine: NDArray) -> csc_array:
    """ Returns coarse cg to dg map interpolated in fine nodes.

    Parameters
    ----------
    nelems_coarse : NDArray
        Number of coarse elements per dimension.
    nelems_refine : NDArray
        Number of fine elements per dimension per coarse element.

    Returns
    -------
    csc_array
        Coarse cg to dg map interpolated in fine nodes.
    """
    d = np.size(nelems_coarse)
    nelems_fine = nelems_coarse * nelems_refine
    J = np.arange(np.prod(nelems_coarse*(nelems_refine+1)),
                  dtype=np.int32)
    if d == 1:
        K = np.arange(nelems_refine[0]+1)\
            + nelems_refine[0]*np.arange(nelems_coarse[0])[:, None]
    elif d == 2:
        K = np.arange(nelems_refine[0]+1)\
            + (nelems_fine[0]+1)\
            * np.arange(nelems_refine[1]+1)[:, None]
        K = K.flatten()\
            + (nelems_refine[0]*np.arange(nelems_coarse[0])
               + nelems_refine[1]*(nelems_fine[0]+1)
               * np.arange(nelems_coarse[1])[:, None]).reshape(-1, 1)
    return csc_array((np.ones_like(J), (J, K.flatten())))


def get_cg2dg0(nelems: NDArray, cg2dg: csc_array) -> csr_array:
    """ Returns the cg (p=1) to dg (p=0) matrix.

    Parameters
    ----------
    nelems : NDArray
        Number of elements per dimension.
    cg2dg : csc_array
        Oridnary cg to dg map.

    Returns
    -------
    csr_array
        cg (p=1) to dg (p=0) matrix.
    """
    d = np.size(nelems)
    return kron(eye_array(np.prod(nelems)), np.ones((1, 2**d))/(2**d),
                format='csr').dot(cg2dg)


def averaging(cg2dg: csc_array) -> csc_array:
    """ cg to dg map with averaging on a patch.

    Parameters
    ----------
    Elements : NDArray
        Element array.
    PatchIndices : NDArray
        Node indices within the patch.
    cg2dg : csc_array
        cg to dg map on the patch.

    Returns
    -------
    csc_array
        cg to dg averaging map on a patch.
    """
    C = csc_array(cg2dg.T)
    # We compute the number of elements around the patch nodes to mimic
    # the global averaging operator restricted to the patch.
    N = np.sum(C.toarray(), axis=1, dtype=plodwave.config.PRECISION)
    return csc_array(diags_array(1/N) @ C)


# ======================================================================
# Definition of the local stiffness and mass matrices
# ======================================================================


def loc_stiffness(nelems: NDArray,
                  domain: NDArray) -> NDArray:
    """ Returns the local stiffness matrix on one element.

    Parameters
    ----------
    nelems : NDArray
        Number of elements per dimension.
    domain : NDArray
        Array with the domain boundary values.

    Returns
    -------
    NDArray
        Local stiffness matrix on one element.
    """
    d = np.size(nelems)
    B = (domain[1, :] - domain[0, :])/nelems
    if d == 1:
        S = np.array([[1, -1],
                      [-1, 1]], dtype=plodwave.config.PRECISION)/B[0]
    elif d == 2:
        # integral of first component of the gradient:
        S = np.array([[2, -2, -1, 1],
                      [-2, 2, 1, -1],
                      [-1, 1, 2, -2],
                      [1, -1, -2, 2]],
                     dtype=plodwave.config.PRECISION)/6/(B[0]**2)
        # integral of second component of the gradient:
        S += np.array([[2, 1, -1, -2],
                       [1, 2, -2, -1],
                       [-1, -2, 2, 1],
                       [-2, -1, 1, 2]],
                      dtype=plodwave.config.PRECISION)/6/(B[1]**2)
        S *= np.prod(B)  # = det B
    return S


def loc_mass(nelems: NDArray,
             domain: NDArray) -> NDArray:
    """ Returns the local mass matrix on one element.

    Parameters
    ----------
    nelems : NDArray
        Number of elements per dimension.
    domain : NDArray
        Array with the domain boundary values.

    Returns
    -------
    NDArray
        Local mass matrix on one element.
    """
    d = np.size(nelems)
    if d == 1:
        M = np.array([[2, 1],
                      [1, 2]], dtype=plodwave.config.PRECISION)/6
    elif d == 2:
        M = np.array([[4, 2, 1, 2],
                      [2, 4, 2, 1],
                      [1, 2, 4, 2],
                      [2, 1, 2, 4]], dtype=plodwave.config.PRECISION)/36
    return np.prod((domain[1, :] - domain[0, :])/nelems) * M


def loc_mass_inv(nelems: NDArray,
                 domain: NDArray) -> NDArray:
    """ Returns the local inverse mass matrix on one element.

    Parameters
    ----------
    nelems : NDArray
        Number of elements per dimension.
    domain : NDArray
        Array with the domain boundary values.

    Returns
    -------
    NDArray
        Local inverse mass matrix on one element.
    """
    d = np.size(nelems)
    if d == 1:
        M = np.array([[2, -1],
                      [-1, 2]], dtype=plodwave.config.PRECISION)*2
    elif d == 2:
        M = np.array([[4, -2, 1, -2],
                      [-2, 4, -2, 1],
                      [1, -2, 4, -2],
                      [-2, 1, -2, 4]], dtype=plodwave.config.PRECISION)*4
    return np.prod(nelems/(domain[1, :] - domain[0, :])) * M


# ======================================================================
# Assembly of the global matrices
# ======================================================================


def assemble_matrices(nelems: NDArray,
                      loc: NDArray,
                      mesh_type: str = 'dg',
                      cg2dg: csc_array | None = None,
                      weight: NDArray | None = None) -> csr_array:
    """ Returns a global cg or dg matrix based on the local matrix loc,
    with weights.

    Parameters
    ----------
    nelems : NDArray
        Number of elements per dimension.
    loc : NDArray
        Local matrix.
    mesh_type : str, optional
        Choice between 'cg' or 'dg' matrix, by default 'dg'.
    cg2dg : csc_array | None, optional
        cg to dg matrix that is needed for the cg assembly, by default
        None.
    weight : NDArray | None, optional
        Weighting the local matrices, by default None which represents
        constant 1 coefficient.

    Returns
    -------
    csr_array
        Global matrix.
    """
    if weight is None:
        weight = np.ones((np.prod(nelems),), dtype=plodwave.config.PRECISION)
    assert np.size(weight) == np.prod(nelems), 'The coefficient is not'\
        + ' properly defined for the underlying fine mesh.'
    if weight.ndim == 2:
        weight = weight.flatten()
    if mesh_type == 'dg':
        return _dg_assembly(loc, weight)
    return _cg_assembly(cg2dg, loc, weight)


def _dg_assembly(loc: NDArray,
                 weight: NDArray) -> csr_array:
    """ Assmebles the global dg matrix based on loc with weigths.

    Parameters
    ----------
    loc : NDArray
        Local matrix.
    weight : NDArray
        Weight per element.

    Returns
    -------
    csr_array
        Global matrix.
    """
    return csr_array(kron(diags_array(weight), loc))


def _cg_assembly(cg2dg: NDArray,
                 loc: NDArray,
                 weight: NDArray) -> csr_array:
    """ Assmebles the global cg matrix based on loc with weigths.

    Parameters
    ----------
    NElems : NDArray
        Number of elements per dimension.
    loc : NDArray
        Local matrix.
    weight : NDArray
        Weight per element.

    Returns
    -------
    csr_array
        Global matrix.
    """
    dg_matrix = _dg_assembly(loc, weight)
    return csr_array(cg2dg.T @ (dg_matrix @ cg2dg))
